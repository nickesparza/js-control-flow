const lang = "es"

if (lang === "en") {
    console.log("Hello World")
} else if (lang === "fr") {
    console.log("Bonjour le monde")
} else if (lang === "it") {
    console.log("Ciao mondo")
} else if (lang === "es") {
    console.log("Hola el mundo")
} else if (lang === "du") {
    console.log("Hallo Wereld")
} else {
    console.log("Sorry, we don't support that language yet.")
}